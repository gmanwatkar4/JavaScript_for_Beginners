// switch statement

// passing number

const month = 3

switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");   // march
        break;
    case 4:
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}

// passing string 

const myMonth = "march"
switch (myMonth) {
    case "january":
        console.log("january");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");   // march
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}